    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:        
        if("".equals(no.getText()) || "".equals(nama.getText()) || "".equals(alamat_peminjam.getText()) || "".equals(judul.getText()) || "".equals(tgl_pinjam) || "".equals(tgl_kembali) || "".equals(status.getText()))
        {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } 
        else
        {   
            String jenis = "";
                if(bacaan.isSelected()){
                    jenis = "Bacaan";
                } else if(pelajaran.isSelected()){
                    jenis = "Pelajaran";
                } else if(novel.isSelected()){
                    jenis = "Novel";
                }
            String SQL = "INSERT INTO tb_pengembalian (NoAnggota,NamaPeminjam,AlamatPeminjam,JenisBuku,JudulBuku,tgl_pinjam,tgl_kembali,status)" + "VALUES('" + no.getText()+"','"+ nama.getText()+"','"+ alamat_peminjam.getText()+"','"+ jenis +"','"+ judul.getText()+"','"+ tgl_pinjam.getDate() +"','"+ tgl_kembali.getDate() +"','"+ status.getText()+"')";
            int status = KoneksiDB.execute(SQL);
            if(status==1){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else{
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_saveActionPerformed

