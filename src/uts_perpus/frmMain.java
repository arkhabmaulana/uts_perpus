    private void selectData() {
        String kolom[] = {"NoAnggota","NamaPeminjam","AlamatPeminjam","JenisBuku","JudulBuku","tgl_pinjam","tgl_kembali","status"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_pengembalian";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String NoAnggota = rs.getString(1);
                String NamaPeminjam = rs.getString(2);
                String AlamatPeminjam = rs.getString(3);
                String JenisBuku = "";
                if("Bacaan".equals(rs.getString(4))) {
                    JenisBuku = "Bacaan";
                } else{
                    JenisBuku = "Pelajaran";
                } 
                String JudulBuku = rs.getString(5);
                String tgl_pinjam = rs.getString(6);
                String tgl_kembali = rs.getString(7);
                String status = rs.getString(8);
                String data[] = {NoAnggota,NamaPeminjam,AlamatPeminjam,JenisBuku,JudulBuku,tgl_pinjam,tgl_kembali,status};
                dtm.addRow(data);
            }
        } catch(SQLException ex){
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        tabel.setModel(dtm);
    }
